let daysOfTheWeek = prompt ("Enter the day of the week : 1 for Monday, 2 for Tuesday, 3 for Wednesday, 4 for Thursday, 5 for Friday, 6 for Saturday, 7 for Sunday");

switch (daysOfTheWeek) {

    case "1":
        alert("Monday");
        break;

    case "2":
        alert("Tuesday");
        break;

    case "3":
        alert("Wednesday");
        break;

    case "4":
        alert("Thursday");
        break;

    case "5":
        alert("Friday");
        break;

    case "6":
        alert("Saturday");
        break;

    case "7":
        alert("Sunday");
        break;

    default:
        alert("Invalid input");

        

} // o switch é usado quando queremos fazer uma escolha entre várias opções que ja temos.