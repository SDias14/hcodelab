//switch case é uma estrutura que permite que você escolha entre várias opções


let position = 1

switch (position) {

    case 1:
        console.log('Voce esta na posição com o cavalo')
        break;
    case 2:
        console.log('Voce esta na posição sem o cavalo')
        break;
        default:
            console.log('Voce não esta na posição')
            break;
}


